package com.syntax.class01;

public class PracticeVariables {

public static void main (String[] args) {
		
		byte b = 12;          			     // from -128    
		short s = 32767;       		         // from -32768
		int i = 2_147_483_647;           	 // from 2147483647
		long l = 9_223_372_036_854_775_807L; // from -9_223_372_036_854_775_808
		float f = 17.99F;		             // from  
		double d = 15.99;					 // from
		char ch1 = 'G';						 // from
		char ch2 = 'U';						 // from
		boolean bo1 = true;   				 // from
		boolean bo2 = false;				 //	from
		
		System.out.println("byte:     " + b);
		System.out.println("short:    " + s);
		System.out.println("int:      " + i);
		System.out.println("long:     " + l);
		System.out.println("float:    " + f);
		System.out.println("double:   " + d);
		System.out.println("char:     " + ch1);
		System.out.println("char2:    " + ch2);
		System.out.println("boolean:  " + bo1);
		System.out.println("boolean2: " + bo2);
	}
	
}
